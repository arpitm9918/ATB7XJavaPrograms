package ex21072024;

public class lab113 {
    public static void main(String[] args) {
        for (int a=0; a <=10; a++)
        {
            System.out.println("Number is--"+ a);
            //supported data type in for loop int, char, float, double, long, byte
        }
        for (long i=1; i <10; i++){
            System.out.println(i);
        }
        for (byte b=0; b<10; b++){
            System.out.println(b);
        }
    }
}
